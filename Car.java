public class Car {
    // Attributes (state)
    private String color;
    private String model;
    // Constructor
    public Car(String color, String model) {
        this.color = color;
        this.model = model;
    }
    // Getters
    public String getColor() {
        return color;
    }

    public String getModel() {
        return model;
    }
}
